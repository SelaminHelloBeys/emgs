// Get all vocabulary as flat array for global search
const getAllVocabulary = () => {
  const allWords: Array<{ word: string; meaning: string; example: string; level: string }> = [];
  Object.entries(VOCABULARY).forEach(([level, words]) => {
    words.forEach(word => {
      allWords.push({ ...word, level });
    });
  });
  return allWords;
};

interface EnglishVocabularySectionProps {
  onBack: () => void;
}

